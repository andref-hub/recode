/*
   1 -> iniciar
   2-> pausar
   3 -> avançar
   4 -> retroceder
   5 -> encerrar
*/

let numero = 3;

if (numero == 1) {
    console.log("INICIAR");
} else if (numero == 2) {
    console.log("PAUSAR")
} else if (numero == 3) {
    console.log("AVANÇAR")
} else if (numero == 4) {
    console.log("RETROCEDER")
} else if (numero == 5) {
    console.log("ENCERRAR")
} else {
    console.log("RESPOSTA INVÁLIDA")
}