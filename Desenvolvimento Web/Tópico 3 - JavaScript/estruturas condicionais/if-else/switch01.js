/*
   1 -> iniciar
   2-> pausar
   3 -> avançar
   4 -> retroceder
   5 -> encerrar
*/

let numero = 1;

switch (numero) {
    case 1:
    console.log("INICIAR");
    break;

    case 2:
    console.log("PAUSAR");
    break;

    case 3:
    console.log("AVANÇAR");
    break;

    case 4:
    console.log("RETROCEDER");
    break;

    case 5:
    console.log("ENCERRAR");
    break;

    default:
        console.log("RESPOSTA INVÁLIDA!")
}